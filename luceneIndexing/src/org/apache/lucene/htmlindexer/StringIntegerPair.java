package org.apache.lucene.htmlindexer;

public class StringIntegerPair { 
	private String name; 
	private int Index; 
 
	public String getName() { 
		return name; 
	} 
	public StringIntegerPair()
	{
		
	}
 
	public int getIndex() { 
		return Index; 
	} 
 
	public void setName(String name) { 
		this.name = name; 
	} 
 
	public void setIndex(int Index) { 
		this.Index = Index; 
	} 
} 